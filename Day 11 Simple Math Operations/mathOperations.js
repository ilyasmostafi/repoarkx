

function addNumbers(num1,num2){
    return parseInt(num1) + parseInt(num2);
}
function subNumbers(num1,num2){
    return parseInt(num1) - parseInt(num2);
}
function multiplyNumbers(num1,num2){
    return parseInt(num1) * parseInt(num2);
}
function devideNumbers(num1,num2){
    return parseInt(num1) / parseInt(num2);
}


